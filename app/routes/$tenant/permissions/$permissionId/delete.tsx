import { data, redirect, useLoaderData, useActionData, Form, Link } from "react-router";

export const handle = { breadcrumb: "Delete Permission" };

import { requirePermission } from "~/lib/require-auth.server";
import {
  getPermissionWithCounts,
  deletePermission,
  PermissionError,
} from "~/services/permissions.server";
import { Button } from "~/components/ui/button";
import { Card, CardContent, CardHeader, CardTitle } from "~/components/ui/card";
import { useBasePrefix } from "~/hooks/use-base-prefix";
import type { Route } from "./+types/delete";

export async function loader({ request, params }: Route.LoaderArgs) {
  await requirePermission(request, "settings", "manage");
  const permission = await getPermissionWithCounts(params.permissionId);
  return { permission };
}

export async function action({ request, params }: Route.ActionArgs) {
  const { user } = await requirePermission(request, "settings", "manage");
  const tenantId = user.tenantId;
  if (!tenantId) {
    throw data({ error: "User is not associated with a tenant" }, { status: 403 });
  }

  const ctx = {
    userId: user.id,
    tenantId,
    ipAddress: request.headers.get("x-forwarded-for") ?? undefined,
    userAgent: request.headers.get("user-agent") ?? undefined,
  };

  try {
    await deletePermission(params.permissionId, ctx);
    return redirect(`/${params.tenant}/permissions`);
  } catch (error) {
    if (error instanceof PermissionError) {
      return data({ error: error.message }, { status: error.status });
    }
    throw error;
  }
}

export default function DeletePermissionPage() {
  const { permission } = useLoaderData<typeof loader>();
  const actionData = useActionData<typeof action>();
  const basePrefix = useBasePrefix();

  const canDelete = permission._count.rolePermissions === 0;
  const assignedRoles = permission.rolePermissions.map((rp) => rp.role);

  return (
    <div className="space-y-6">
      <div>
        <h2 className="text-2xl font-bold text-foreground">Delete Permission</h2>
        <p className="mt-1 text-sm text-muted-foreground">
          Review the details below before deleting this permission.
        </p>
      </div>

      {actionData && "error" in actionData && (
        <div className="rounded-md bg-destructive/10 p-3 text-sm text-destructive">
          {actionData.error}
        </div>
      )}

      <Card>
        <CardHeader>
          <CardTitle>
            {permission.resource}:{permission.action}
          </CardTitle>
        </CardHeader>
        <CardContent className="space-y-4">
          <div className="grid grid-cols-2 gap-4 text-sm">
            <div>
              <span className="font-medium text-foreground">Resource</span>
              <p className="text-muted-foreground">{permission.resource}</p>
            </div>
            <div>
              <span className="font-medium text-foreground">Action</span>
              <p className="text-muted-foreground">{permission.action}</p>
            </div>
            {permission.description && (
              <div className="col-span-2">
                <span className="font-medium text-foreground">Description</span>
                <p className="text-muted-foreground">{permission.description}</p>
              </div>
            )}
            <div>
              <span className="font-medium text-foreground">Assigned Roles</span>
              <p className="text-muted-foreground">{permission._count.rolePermissions}</p>
            </div>
          </div>

          {!canDelete && (
            <div className="rounded-md bg-destructive/10 p-3 text-sm text-destructive">
              <p>
                Cannot delete this permission because it is assigned to{" "}
                {permission._count.rolePermissions} role(s):
              </p>
              <ul className="mt-1 list-disc pl-5">
                {assignedRoles.map((role) => (
                  <li key={role.id}>{role.name}</li>
                ))}
              </ul>
              <p className="mt-1">Unassign from all roles first.</p>
            </div>
          )}

          {canDelete && (
            <div className="rounded-md bg-destructive/10 p-3 text-sm text-destructive">
              This action cannot be undone. The permission will be permanently removed.
            </div>
          )}

          <div className="flex gap-3 pt-2">
            {canDelete ? (
              <Form method="post">
                <Button type="submit" variant="destructive">
                  Delete Permission
                </Button>
              </Form>
            ) : (
              <Button variant="destructive" disabled>
                Delete Permission
              </Button>
            )}
            <Button variant="outline" asChild>
              <Link to={`${basePrefix}/permissions`}>Cancel</Link>
            </Button>
          </div>
        </CardContent>
      </Card>
    </div>
  );
}
